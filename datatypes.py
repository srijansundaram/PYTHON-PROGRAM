# Numeric types: int, float, complex
integer_number = 10
float_number = 3.14
complex_number = 2 + 3j

# Sequence types: str, list, tuple
string_variable = "Hello, World!"
list_variable = [1, 2, 3, 4]
tuple_variable = (5, 6, 7, 8)

# Mapping type: dict
dictionary_variable = {'name': 'Alice', 'age': 30}

# Set types: set, frozenset
set_variable = {1, 2, 3, 4}
frozenset_variable = frozenset({5, 6, 7, 8})

# Boolean type: bool
boolean_variable = True

# NoneType: None
none_variable = None

# Printing out the values to demonstrate each type
print("Numeric Types:")
print(f"Integer: {integer_number}, type: {type(integer_number)}")
print(f"Float: {float_number}, type: {type(float_number)}")
print(f"Complex: {complex_number}, type: {type(complex_number)}\n")

print("Sequence Types:")
print(f"String: {string_variable}, type: {type(string_variable)}")
print(f"List: {list_variable}, type: {type(list_variable)}")
print(f"Tuple: {tuple_variable}, type: {type(tuple_variable)}\n")

print("Mapping Type:")
print(f"Dictionary: {dictionary_variable}, type: {type(dictionary_variable)}\n")

print("Set Types:")
print(f"Set: {set_variable}, type: {type(set_variable)}")
print(f"Frozenset: {frozenset_variable}, type: {type(frozenset_variable)}\n")

print("Boolean Type:")
print(f"Boolean: {boolean_variable}, type: {type(boolean_variable)}\n")

print("NoneType:")
print(f"None: {none_variable}, type: {type(none_variable)}")
